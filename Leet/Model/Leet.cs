namespace Leet
{
  public class LeetSpeak
  {
    public bool IsLeet(char letter)
    {
      if (letter == 'e')
      {
        return true;
      }
      else if (letter == 'o')
      {
        return true;
      }
      else if (letter == 'I')
      {
        return true;
      }
      else if (letter == 't')
      {
        return true;
      }
      else if (letter == 's')
      {
        return true;
      }
      return false;
    }
  }
}
